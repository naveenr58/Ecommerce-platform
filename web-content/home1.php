<h1>Wear Your Attitude</h1>
<div id="mater">Whether we choose to consciously compute the fact or not, how we look does impact the way we feel. It may be that we dress to a theme that evokes a mood, that we choose to pull on something that incites a certain confidence within us, or that we – for no definable reason at all – simply fall in love with a particular piece of clothing or accessory. Either way, the reality is that a large part of how our style evolves each year has to do with the clothing, hair, beauty and accessory trends sweeping fashion, sweeping the runways or the magazine pages or the streets. Not because we have to follow them, but because they inspire us. Each year we analyse what’s happening in the fashion world as a way to understand where the trends are coming from, where they’re going, and – should you so choose – how you can adapt them to your own personal style, in a way that suits you.<br />
<h2>Suggestions</h2>
Dark colours attract heat, so the last thing you want to wear in the spring or summer is black, and come winter, you want all the heat you can get. Secondly, it complements the season. Gloomier days, earlier nights, and a general lack of light warrant a response of equal darkness – so pile on the purples and blacks and whatever dark tone you’re into.
 
Technically speaking, tweed is a woven wool fabric, so you can understand why it’s a no-go during warmer months (we would all die of heat exhaustion). That said, after weeks and weeks of not being worn, it becomes a welcome reprieve after periods of hot, sticky temperatures, cotton blends, and maxi dresses.
Now, you can comfortably wear tweed blazers, tweed pants, and tweed skirts without fearing heatstroke (or the desire to lock yourself in an icebox) – and you can layer underneath it, too.
 
Plaid and/or tartan got its start in Scotland years ago, so consider it another functional fabric. Traditionally worn as part of the traditional Scottish costume, it’s since found its way over here where it’d been adapted in droves. Why? For starters, it’s practical, it’s easy to make, and you can wrap and layer it (which came especially handy during those cold Canadian winters). And considering it’s now a part of the Canadian uniform (we all own at least one plaid), with so much ‘90s nostalgia this season, it makes sense to throw an oversize button-up over a mini-dress for both aesthetic and functional purposes.</div>

